export const t = (lang: LanguageCode, key: string): string => {
  // Try exact match
  if (translations[lang] && translations[lang][key]) {
    return translations[lang][key];
  }
  // Fallback to English
  if (translations['en'][key]) {
    return translations['en'][key];
  }
  return key;
};

